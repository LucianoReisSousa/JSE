package br.com.wincomp.br;

public class DemoOperadoresRelacionais {

	public static void main(String[] args) {

		int op1 = 0;
		int op2 = 0;
		
		op1 = 10;
		op2 = 5;
		System.out.println(op1 > op2);
		
		op1 = 10;
		op2 = 10;
		System.out.println(op1 >= op2);
		
		op1 = 5;
		op2 = 10;
		System.out.println(op1 < op2);
		
		op1 = 5;
		op2 = 5;
		System.out.println(op1 <= op2);
		
		op1 = 5;
		op2 = 5;
		System.out.println(op1 == op2);
		
		op1 = 5;
		op2 = 10;
		System.out.println(op1 != op2);
		
		
	}

}
