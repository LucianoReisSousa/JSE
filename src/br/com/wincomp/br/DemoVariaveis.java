package br.com.wincomp.br;

public class DemoVariaveis {

	public static void main(String[] args) {
	
		int i = 10;
		double d = 9.95;
		char c = 'S';
		boolean b = true;
		
		System.out.println("i: " +i);
		System.out.println("d: " +d);
		System.out.println("c: " +c);
		System.out.println("b: " +b);
		

	}

}